// 7.	Игра «Угадай число». Предложить пользователю загадать число от 0 до 100 и отгадать его следующим способом: 
// каждую итерацию цикла делите диапазон чисел пополам, записываете результат в N и спрашиваете у пользователя 
// «Ваше число> N, <N или == N?». В зависимости от того, что указал пользователь, уменьшаете диапазон. Начальный диапазон от 0 до 100, 
// поделили пополам и получили 50. Если пользователь указал, что его число> 50, то изменили диапазон на от 51 до 100. И так до тех пор, 
// пока пользователь не выберет == N.


let minRange = 0
let maxRange = 100
let bl = true

while (bl) {
    let g = Math.floor((minRange + maxRange) / 2)
    let userInput = prompt(`Ваше число > ${g}, < ${g} или == ${g}? (Введите >, < или =)`)

    if (userInput === '>') {
        minRange = g + 1
    } else if (userInput === '<') {
        maxRange = g - 1
    } else if (userInput === '=') {
        bl = false
        alert(`Ваше число равно ${g}!`)
    } else {
        alert('Пожалуйста, введите >, < или =')
    }

    if (minRange > maxRange) {
        alert('Что-то пошло не так! Возможно, вы ошиблись при ответах.')
        break
    }
}